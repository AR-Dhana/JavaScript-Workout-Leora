let myMark=81;

if ((myMark>=91)&&(myMark<=100))
{
    console.log("A");
}
else if ((myMark>=81)&&(myMark<=90)) {
    console.log("B");
}
else if ((myMark>=71)&&(myMark<=80)) {
    console.log("C");
}
else if ((myMark>=61)&&(myMark<=70)) {
    console.log("D");
}
else if ((myMark>=51)&&(myMark<=60)) {
    console.log("E");
}
else if ((myMark>=0)&&(myMark<=50)) {
    console.log("F");
}
else
{
    console.log("Invalid Mark");
}